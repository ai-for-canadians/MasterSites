// Helper functions
export function getOrganization(slug: string): Organization | undefined {
    return organizations.find(o => o.slug === slug);
}

export function getOrganizationsByType(type: Organization['type']): Organization[] {
    return organizations.filter(o => o.type === type).sort((a, b) => b.overallScore - a.overallScore);
}

export function getNonprofits(): Organization[] {
    return getOrganizationsByType('nonprofit');
}

export function getCompanies(): Organization[] {
    return organizations
        .filter(o => o.type === 'bcorp' || o.type === 'public-company' || o.type === 'private-company')
        .sort((a, b) => b.overallScore - a.overallScore);
}

export function getTopOrganizations(limit: number = 10): Organization[] {
    return [...organizations].sort((a, b) => b.overallScore - a.overallScore).slice(0, limit);
}
