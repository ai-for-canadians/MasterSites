// Helper functions
export function getCountryById(id: string): Country | undefined {
  return countries.find((c) => c.id === id);
}

export function getCountryByCode(code: string): Country | undefined {
  return countries.find((c) => c.code === code);
}

export function getCountriesByRegion(region: string): Country[] {
  return countries.filter((c) => c.region === region);
}

export function getCountriesSorted(sortBy: keyof Country['scores'] = 'overall'): Country[] {
  return [...countries].sort((a, b) => b.scores[sortBy] - a.scores[sortBy]);
}

export function getTopCountries(n: number = 5): Country[] {
  return getCountriesSorted().slice(0, n);
}

export function getBottomCountries(n: number = 5): Country[] {
  return getCountriesSorted().slice(-n).reverse();
}

export function getImprovingCountries(): Country[] {
  return countries.filter((c) => c.trend === 'improving');
}

export function getDecliningCountries(): Country[] {
  return countries.filter((c) => c.trend === 'declining');
}
