export function getSubstanceById(id: string): Substance | undefined {
  return substances.find((s) => s.id === id);
}

export function getSubstanceBySlug(slug: string): Substance | undefined {
  return substances.find((s) => s.slug === slug);
}

export function getSubstancesByCategory(category: string): Substance[] {
  return substances.filter(
    (s) => s.category === category || s.categorySecondary === category
  );
}

export function searchSubstances(query: string): Substance[] {
  const q = query.toLowerCase();
  return substances.filter(
    (s) =>
      s.name.toLowerCase().includes(q) ||
      s.aliases.some((alias) => alias.toLowerCase().includes(q)) ||
      s.description.toLowerCase().includes(q)
  );
}
